
vim.opt.encoding = "utf-8" -- set encoding
vim.opt.nu = true -- enable line numbers
vim.opt.relativenumber = true -- relative line numbers

vim.opt.tabstop = 2
vim.opt.softtabstop = 2
vim.opt.shiftwidth = 2
vim.opt.expandtab = true -- convert tabs to spaces
vim.opt.autoindent = true -- auto indent
vim.opt.list = true -- show tab char and trailing whitespace

vim.opt.ignorecase = true -- ignore case on search
vim.opt.smartcase = true -- unless capital letter on search

vim.opt.hlsearch = false -- do not highlight matches on prev search pattern
vim.opt.incsearch = true -- incrementally highlight searches as typing
